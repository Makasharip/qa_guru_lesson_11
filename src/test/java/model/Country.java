package model;

import java.util.List;

public class Country {
    private String name;
    private String size;
    private int age;
    private List<String> notes;

    public String getName() {
        return name;
    }

    public String getSize() {
        return size;
    }

    public int getAge() {
        return age;
    }

    public List<String> getNotes() {
        return notes;
    }


}
